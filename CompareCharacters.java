class CompareCharacters {
    public static void main(String[] args) {
        char char1 = 'A';
        char char2 = 'B';
        System.out.println(char1 + " < " + char2 + " is " + (char1 < char2));
        System.out.println(char1 + " <= " + char2 + " is " + (char1 <= char2));
        System.out.println(char1 + " > " + char2 + " is " + (char1 > char2));
        System.out.println(char1 + " >= " + char2 + " is " + (char1 >= char2));
        System.out.println(char1 + " == " + char2 + " is " + (char1 == char2));
        System.out.println(char1 + " != " + char2 + " is " + (char1 != char2));
    }
}
